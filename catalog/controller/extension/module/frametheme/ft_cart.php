<?php
class ControllerExtensionModuleFramethemeFTCart extends Controller {
	public function index() {
		$this->load->language('extension/module/frametheme/ft_cart');
		$this->load->language('extension/module/frametheme/ft_global');

		$this->load->model('setting/setting');

		$ft_settings = array();
		$ft_settings = $this->model_setting_setting->getSetting('theme_frame', $this->config->get('config_store_id'));

		if (isset($ft_settings['t1_high_definition_imgs']) && $ft_settings['t1_high_definition_imgs']){
			$hd_imgs = $ft_settings['t1_high_definition_imgs'];
		} else {
			$hd_imgs = false;
		}

		// Totals
		$this->load->model('setting/extension');

		$totals = array();
		$taxes = $this->cart->getTaxes();
		$total = 0;

		// Because __call can not keep var references so we put them into an array.
		$total_data = array(
			'totals' => &$totals,
			'taxes'  => &$taxes,
			'total'  => &$total
		);

		// Display prices
		if ($this->customer->isLogged() || !$this->config->get('config_customer_price')) {
			$sort_order = array();

			$results = $this->model_setting_extension->getExtensions('total');

			foreach ($results as $key => $value) {
				$sort_order[$key] = $this->config->get('total_' . $value['code'] . '_sort_order');
			}

			array_multisort($sort_order, SORT_ASC, $results);

			foreach ($results as $result) {
				if ($this->config->get('total_' . $result['code'] . '_status')) {
					$this->load->model('extension/total/' . $result['code']);

					// We have to put the totals in an array so that they pass by reference.
					$this->{'model_extension_total_' . $result['code']}->getTotal($total_data);
				}
			}

			$sort_order = array();

			foreach ($totals as $key => $value) {
				$sort_order[$key] = $value['sort_order'];
			}

			array_multisort($sort_order, SORT_ASC, $totals);
		}

		if (isset($this->request->get['route']) && ($this->request->get['route'] == 'checkout/cart' || $this->request->get['route'] == 'checkout/simplecheckout')) {
			$data['in_cart'] = true;
		} else {
			$data['in_cart'] = false;
		}

		$data['text_items'] = sprintf(html_entity_decode($this->language->get('g_text_items'), ENT_QUOTES, 'UTF-8'), $this->cart->countProducts() + (isset($this->session->data['vouchers']) ? count($this->session->data['vouchers']) : 0), $this->currency->format($total, $this->session->data['currency']));

		$this->load->model('tool/image');
		$this->load->model('tool/upload');

		$data['products'] = array();

		foreach ($this->cart->getProducts() as $product) {

			$img_width = $this->config->get('theme_' . $this->config->get('config_theme') . '_image_cart_width');
			$img_height = $this->config->get('theme_' . $this->config->get('config_theme') . '_image_cart_height');

			if ($product['image']) {
				$image = $this->model_tool_image->resize($product['image'], $this->config->get('theme_' . $this->config->get('config_theme') . '_image_cart_width'), $this->config->get('theme_' . $this->config->get('config_theme') . '_image_cart_height'));

				if ($hd_imgs) {
					$image2x = $this->model_tool_image->resize($product['image'], $this->config->get('theme_' . $this->config->get('config_theme') . '_image_cart_width')*2, $this->config->get('theme_' . $this->config->get('config_theme') . '_image_cart_height')*2);
					$image3x = $this->model_tool_image->resize($product['image'], $this->config->get('theme_' . $this->config->get('config_theme') . '_image_cart_width')*3, $this->config->get('theme_' . $this->config->get('config_theme') . '_image_cart_height')*3);
					$image4x = $this->model_tool_image->resize($product['image'], $this->config->get('theme_' . $this->config->get('config_theme') . '_image_cart_width')*4, $this->config->get('theme_' . $this->config->get('config_theme') . '_image_cart_height')*4);
				}

			} else {
				$image = '';

				if ($hd_imgs) {
					$image2x = '';
					$image3x = '';
					$image4x = '';
				}
			}

			$option_data = array();

			foreach ($product['option'] as $option) {
				if ($option['type'] != 'file') {
					$value = $option['value'];
				} else {
					$upload_info = $this->model_tool_upload->getUploadByCode($option['value']);

					if ($upload_info) {
						$value = $upload_info['name'];
					} else {
						$value = '';
					}
				}

				$option_data[] = array(
					'name'  => $option['name'],
					'value' => (utf8_strlen($value) > 20 ? utf8_substr($value, 0, 20) . '..' : $value),
					'type'  => $option['type']
				);
			}

			// Display prices
			if ($this->customer->isLogged() || !$this->config->get('config_customer_price')) {
				$unit_price = $this->tax->calculate($product['price'], $product['tax_class_id'], $this->config->get('config_tax'));

				$price = $this->currency->format($unit_price, $this->session->data['currency']);
				$total = $this->currency->format($unit_price * $product['quantity'], $this->session->data['currency']);
			} else {
				$price = false;
				$total = false;
			}

			$minimum = $product['minimum'] > 0 ? $product['minimum'] : 1;

			$data['products'][] = array(
				'cart_id'   => $product['cart_id'],
				'thumb'     => $image,
				'img_width' => $img_width,
				'img_height' => $img_height,
				'thumb2x'     => $hd_imgs ? $image2x : NULL,
				'thumb3x'     => $hd_imgs ? $image3x : NULL,
				'thumb4x'     => $hd_imgs ? $image4x : NULL,
				'name'      => $product['name'],
				'model'     => $product['model'],
				'option'    => $option_data,
				'recurring' => ($product['recurring'] ? $product['recurring']['name'] : ''),
				'quantity'  => $product['quantity'],
				'quantity_plus'  => (int)$product['quantity'] + 1,
				'quantity_minus'  => (int)$product['quantity'] > $minimum ? (int)$product['quantity'] - 1 : 0 ,
				'price'     => $price,
				'total'     => $total,
				'href'      => $this->url->link('product/product', 'product_id=' . $product['product_id'])
			);
		}

		// Gift Voucher
		$data['vouchers'] = array();

		if (!empty($this->session->data['vouchers'])) {
			foreach ($this->session->data['vouchers'] as $key => $voucher) {
				$data['vouchers'][] = array(
					'key'         => $key,
					'description' => $voucher['description'],
					'amount'      => $this->currency->format($voucher['amount'], $this->session->data['currency'])
				);
			}
		}

		$data['totals'] = array();

		foreach ($totals as $total) {
			$data['totals'][] = array(
				'title' => $total['title'],
				'text'  => $this->currency->format($total['value'], $this->session->data['currency']),
			);
		}

		$data['cart'] = $this->url->link('checkout/cart');
		$data['checkout'] = $this->url->link('checkout/checkout', '', true);

		return $this->load->view('extension/module/frametheme/ft_cart', $data);
	}

	public function info() {
		$this->response->setOutput($this->index());
